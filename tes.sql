-- Procedure 1: Aboneler tablosunda belirli bir kuruma ait aboneleri listele
CREATE PROCEDURE sp_KurumAboneleriniListele
    @KurumID INT
AS
BEGIN
    SELECT * FROM Aboneler WHERE KurumID = @KurumID
END;
GO

-- Procedure 2: Bir kullanıcının gönderdiği SMS toplam tutarını getir
CREATE PROCEDURE sp_KullaniciToplamSMSTutari
    @KullaniciID INT
AS
BEGIN
    SELECT SUM(Tutar) AS ToplamTutar
    FROM SMS
    WHERE GonderenKullaniciID = @KullaniciID
END;
GO

-- Trigger: StokHareket tablosunda yeni giriş olduğunda Varlik tablosunun Durumunu 1 yap
CREATE TRIGGER trg_StokGiris_VarlikAktif
ON StokHareket
AFTER INSERT
AS
BEGIN
    UPDATE Varlik
    SET Durum = 1
    FROM Varlik v
    INNER JOIN inserted i ON v.ID = i.VarlikID
    WHERE i.IslemTipi = 'Giriş'
END;
